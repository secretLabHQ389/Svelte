<script>
	let hovering;

	function enter() {
		hovering = true;
	}

	function leave() {
		hovering = false;
	}
</script>

<article class="contact-card">
	<h2 on:mouseenter={enter} on:mouseleave={leave}>
		<slot name="name" hovering={hovering}>
			<span class="missing">Unknown name</span>
		</slot>
	</h2>

	<div class="address" on:mouseenter={enter} on:mouseleave={leave}>
		<slot name="address" hovering={hovering}>
			<span class="missing">Unknown address</span>
		</slot>
	</div>

	<div class="email" on:mouseenter={enter} on:mouseleave={leave}>
		<slot name="email" hovering={hovering}>
			<span class="missing">Unknown email</span>
		</slot>
	</div>
</article>

